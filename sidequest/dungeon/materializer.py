"""Beneath Sünden materializer — the runtime consumer the entire Plan 1–6 stack deferred to.

Every prior Sünden plan (1–6) ended with an explicit honest-deferral pointing here:
Plans 1–6 are pure substrate (generator, persistence, set-piece/trope/quest attach) with
zero non-test runtime consumers until THIS module.  This module is the keystone.  Tasks
1–8 (Plan 7) implement it completely.

-----
Pipeline shape
-----

``materialize(request, ...)`` is the five-stage async coordinator::

    design → fill → curate → attach → commit

All stages run inside a parent ``dungeon.materialize`` OTEL span; each stage's child span
nests under it in order.  Only the curate stage is ``await``-ed; the other four are
synchronous calls inside the async coordinator.  The async shape is forced by the SDK
``complete_with_tools`` being a coroutine: a nested ``asyncio.run`` from the running uvicorn
event loop (or from the Task-7 async look-ahead worker) would raise ``RuntimeError``
unconditionally, so ``materialize`` is declared ``async def`` and the curate stage is
``async def _stage_curate``.

``MaterializationRequest`` is a frozen, hashable value object (``@dataclass(frozen=True,
slots=True)``) carrying the full specification for one materialisation run.
``frontier`` is accepted at ``MaterializationRequest.build`` only for validation (confirming
``frontier_edge`` is a member of the live frontier); it is NOT stored as a field so the
hash is stable regardless of how the frontier list grows between construction and use.

-----
Plan 5 binding — persistence transaction (spec §7.5)
-----

The commit stage runs ONE transaction on the **caller-owned** connection accessed via
``persistence._conn``.  This is the sanctioned Plan-5 caller-owned-boundary seam:
``DungeonStore`` deliberately exposes no public ``commit()``/``rollback()``; Plan 5 tests
drive the connection directly (``test_persistence.py`` lines 173/340); Task 6 is the
production caller doing the same.

Seed = Expansion 0: on a fresh save (``load_map`` + ``load_frontier`` both empty — loudly
detected, never assumed) the surface entrance is committed FIRST as
``Expansion(expansion_id=0, new_nodes=[entrance], new_edges=[])`` before the first
generated expansion (``expansion_id >= 1``).  The entrance belongs to no
``Expansion.new_nodes`` generated by the pipeline, so it is never persisted by a generated
``commit_expansion`` — the Seed=Expansion-0 contract is the only path.

On ``PersistError`` the connection is rolled back (``conn.rollback()``) so NO
half-attached expansion / orphan ledger rows survive.  SQLite does NOT auto-rollback on
``IntegrityError``; the explicit rollback is mandatory.

``.rolled`` (the ``RolledSetPiece`` from each ``AttachReport``) is persisted inside this
same transaction via the real Plan-5 append-only primitive
``record_mutation(kind="setpiece_state", ...)``.  It is NEVER recomputed — the save is
truth.  It is persisted EXACTLY as the attach stage produced it (spec §7 freeze target).

**Mask persistence is a documented Plan-5-API gap.**  ``commit_expansion`` persists only
``RegionNode.to_dict()`` (id / expansion_id / theme / depth_score).  There is NO mask BLOB
write path on ``commit_expansion``; fill grids are not threaded to the commit stage; and no
Plan 1–6 code consumes a persisted mask on reload.  The mask is inert until a future plan
adds a mask-BLOB column and a loader.  This is NOT papered over — it is flagged here and
in the Post-Implementation Corrections ledger (item 10).

-----
Plan 6 binding — attach_set_piece coalescence entry point
-----

The attach stage invokes ``attach_set_piece(...)`` once per (region, set_piece) INSIDE the
``dungeon.materialize.attach`` span.  ``attach_set_piece`` is the Plan 6 single coalescence
entry point — it internally rolls, starts tropes, seeds quests, opens threads, and emits
the ``setpiece.attach`` / ``trope.start`` / ``quest.seed`` nested spans.  Plan 7 does NOT
re-emit those; they nest inside the ``dungeon.materialize.attach`` span automatically via
the OTEL context.  Plan 5 emits ``ledger.add`` / ``ledger.resolve`` — also nested
automatically.  Plan 7 owns ONLY ``dungeon.materialize.*`` / ``frontier.expand`` /
``frontier.region_transition`` / ``frontier.lookahead``.

-----
Byte-pinned span contracts (consumed here)
-----

- ``GenerationReport.as_dict()`` → ``dungeon.materialize.design`` span attributes.
  The exact key-set of ``GenerationReport.as_dict()`` is byte-pinned to the design span;
  dict-valued entries are JSON-serialised (``json.dumps(v, sort_keys=True)``).

- ``DepthReport.as_dict()`` → ``dungeon.materialize.attach`` span attributes.
  ``DepthReport.as_dict()`` has **no** ``expansion_id`` / ``stage`` keys (unlike
  ``GenerationReport.as_dict()``); the attach span helper drops the pre-baked ``stage``
  injection.  The 4 scalar keys of ``DepthReport.as_dict()`` are set directly on the span
  (honored at the routed ``SPAN_ROUTES`` extract layer — the established Task-2 through
  Task-6 byte-pinned pattern from ``dungeon_materialize.py``).

-----
Contracts (No Silent Fallbacks / save-is-truth)
-----

- **Curation-failure-is-LOUD**: the one-shot SDK curation pass
  (``complete_with_tools``) failing (``LlmClientError``), returning non-JSON, or
  returning a non-dict ALWAYS raises ``CurationError`` and
  aborts the materialisation transaction.  There is NO fallback that ships the raw
  assembled manifest stamped as curated.  The curate span carries ``curated=false`` +
  ``reason`` before the raise (lie-detector visibility).

- **Save-is-truth**: the raw design + fill output is seed-reproducible (``_region_interior_seed``
  is a deterministic blake2b mixer).  Curation DELIBERATELY breaks byte-reproducibility
  (the LLM pass selects/refines; its output is not seed-deterministic).  Frozen expansions
  and ``.rolled`` set-piece state are NEVER recomputed from seed — if they are in the save
  they are final.

-----
Async pipeline + look-ahead worker
-----

``async def materialize`` is the coordinator; the frontier look-ahead worker
(``lookahead_worker.register_lookahead_worker``) is its async consumer — it ``await``s
``materialize(...)`` directly from inside a background task, which is why the event-loop
ownership must be unambiguous (no nested ``asyncio.run``).

**HONEST AS-BUILT STATUS (do not overclaim):**  ``lookahead_worker.register_lookahead_worker``
is a complete, wiring-tested implementation proven end-to-end against the REAL
``GameSnapshot._apply_world_patch_inner`` → ``notify_region_transition`` producer (non-circular
teeth test, ``tests/dungeon/test_materializer_wiring.py``).  However, it has **ZERO production
callers**: the live WebSocket session lifecycle does NOT yet construct a save-conn
``DungeonStore`` / resolve bundle / palette / pack_tropes / campaign_seed at session-open
and call ``register_lookahead_worker(...)``.  Therefore the ADR-106
"Plans 1–6 are pure substrate with zero non-test runtime consumers" bottleneck is
**REDUCED** (the consuming runtime seam now exists and is proven) but **NOT fully closed**
(no live session activates it; ``observers=0`` on live ``frontier.region_transition`` spans
is the lie-detector signal that the worker is not yet registered).  Live-session
registration is an **OPEN, TRACKED follow-up** requiring a session-integration story —
NOT closed by Plan 7.  Do not read any doc claiming Plan 7 removed the bottleneck
without checking for that story.
"""

