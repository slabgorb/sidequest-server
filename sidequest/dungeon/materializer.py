async def materialize(
    request: MaterializationRequest,
    *,
    graph: RegionGraph | None,
    bundle: Any,
    palette: Any,
    persistence: DungeonStore,
    snapshot: GameSnapshot,
    pack_tropes: Any,
    claude_client: Any,
    is_first_band_entry: bool = True,
) -> None:
    """Run the five-stage materialisation pipeline for one expansion.

    Opens a parent ``dungeon.materialize`` OTEL span; each of the five stage
    spans nests under it in order (design → fill → curate → attach → commit).

    Stages design/fill/curate are implemented (Plan 7 Tasks 2–4); attach
    and commit remain ``NotImplementedError`` until Plan 7 Tasks 5–6.

    ``async def``: the curate stage awaits the SDK
    ``complete_with_tools``. The event loop is owned by the caller
    (uvicorn in prod, pytest-asyncio ``asyncio_mode=auto`` in tests) —
    there is NO ``asyncio.run`` anywhere, so Task 7's async look-ahead
    worker can ``await materialize()`` directly from inside its
    coroutine. design/fill/attach/commit remain plain synchronous calls
    inside this async coordinator; only curate is awaited.

    Parameters
    ----------
    request:
        Frozen expansion specification. ``request.expansion_id`` must be >= 1.
    graph:
        The live ``RegionGraph`` (passed through to attach/commit stages).
    bundle:
        The cookbook bundle (consumed by the curate stage).
    palette:
        The ``ThemePalette`` (consumed by design/fill/curate).
    persistence:
        The ``DungeonStore`` operating on the live save-DB connection
        (caller owns the transaction boundary, spec §7.5).
    snapshot:
        The live ``GameSnapshot`` — the attach stage's
        ``attach_set_piece`` call mutates ``snapshot.active_tropes`` for
        every started trope component (ADR-018 seam). REQUIRED, no silent
        default. Production sourcing (the live session snapshot) is the
        Task-7 session-wiring concern; Task 5 binds the API.
        (Reconcile seam: ``snapshot``.)
    pack_tropes:
        The resolved genre-pack tropes (a duck-typed object with
        ``.tropes``, ADR-018) threaded into the attach stage's
        ``attach_set_piece`` call so trope_ids resolve against the pack.
        REQUIRED, no silent default. Production sourcing (the resolved
        genre-pack) is the Task-7 session-wiring concern; Task 5 binds the
        API. (Reconcile seam: ``pack_tropes``.)
    claude_client:
        Required. The session supplies the SDK client
        (``build_llm_client()`` default ``AnthropicSdkClient``); the
        curate stage issues a one-shot ``complete_with_tools``. Tests
        inject a ``ToolingLlmClient``-shaped fake so no real network call
        is made (DI discipline mirrored from the rest of the codebase —
        NOT a hardcoded non-injectable client; No Silent Fallbacks —
        ``None`` raises, no implicit construction).
    is_first_band_entry:
        Caller-asserted band-entry flag threaded into the curate stage's
        ``assemble_region`` call. The TRUE band-entry-history computation
        needs Plan 5 persistence (``load_frontier``/``load_map``) that the
        curate stage does NOT own; it is deferred to Task 6 (commit, which
        owns persistence). For Task 4 the coordinator passes this
        caller-supplied value (NOT a silent default computed from
        incomplete state). (Reconcile seam: ``is_first_band_entry``.)
    """
    if claude_client is None:
        raise ValueError(
            "materialize requires an explicit claude_client (the look-ahead "
            "worker/session supplies the SDK client via build_llm_client(); "
            "No Silent Fallbacks — no implicit ClaudeClient())"
        )
    curation_client = claude_client

    # The per-region look is derived from each region's theme INSIDE
    # _stage_curate (it has bundle+palette+expansion) — the theme is the
    # single source of truth (loud raise on unresolvable/multi-bound;
    # never a silent default). No look value is threaded from here.
    with dungeon_materialize_span(
        expansion_id=request.expansion_id,
        heading=request.heading,
        burst_magnitude=request.burst_magnitude,
    ):
        with dungeon_materialize_design_span(expansion_id=request.expansion_id) as design_span:
            expansion, _report = _stage_design(
                request, graph=graph, palette=palette, span=design_span
            )

        with dungeon_materialize_fill_span(expansion_id=request.expansion_id) as fill_span:
            fill_result = _stage_fill(request, expansion=expansion, palette=palette, span=fill_span)

        with dungeon_materialize_curate_span(expansion_id=request.expansion_id) as curate_span:
            curation = await _stage_curate(
                request,
                bundle=bundle,
                palette=palette,
                expansion=expansion,
                fill_result=fill_result,
                is_first_band_entry=is_first_band_entry,
                claude_client=curation_client,
                span=curate_span,
            )

        with dungeon_materialize_attach_span(expansion_id=request.expansion_id) as attach_span:
            attach_result = _stage_attach(
                request,
                graph=graph,
                expansion=expansion,
                palette=palette,
                curation=curation,
                snapshot=snapshot,
                pack_tropes=pack_tropes,
                persistence=persistence,
                span=attach_span,
            )

        with dungeon_materialize_commit_span(expansion_id=request.expansion_id) as commit_span:
            # Thread the attach stage's AttachResult into commit: its
            # ``attach_reports[].rolled`` is the spec §7 freeze target Task
            # 6 PERSISTS and NEVER recomputes (save-is-truth). Discarding
            # it here would force Task 6 to re-roll. ``expansion``/``graph``
            # carry the depth-scored attached topology commit_expansion +
            # new-frontier-edge derivation read. ``_stage_commit`` is SYNC
            # (commit_expansion/put_frontier are sync) — a plain call inside
            # this async coordinator, never awaited.
            _stage_commit(
                request,
                graph=graph,
                expansion=expansion,
                attach_result=attach_result,
                persistence=persistence,
                span=commit_span,
            )
