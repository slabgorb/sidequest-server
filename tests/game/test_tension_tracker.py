FIXTURE_DIR = Path(__file__).parent.parent / "fixtures" / "tension"


def _load_fixture(name: str) -> dict:
    path = FIXTURE_DIR / name
    with path.open(encoding="utf-8") as fh:
        return json.load(fh)


def _round_from_fixture(blob: dict) -> RoundResult:
    return RoundResult(
        round=blob["round"],
        damage_events=[
            DamageEvent(
                attacker=d["attacker"],
                target=d["target"],
                damage=d["damage"],
                round=d["round"],
            )
            for d in blob.get("damage_events", [])
        ],
        effects_applied=list(blob.get("effects_applied", [])),
        effects_expired=list(blob.get("effects_expired", [])),
    )


def _classification_from_fixture(blob: dict) -> TurnClassification:
    if blob["kind"] == "Boring":
        return TurnClassification.boring()
    if blob["kind"] == "Normal":
        return TurnClassification.normal()
    if blob["kind"] == "Dramatic":
        event_name = blob["event"]
        return TurnClassification.dramatic(DetailedCombatEvent[event_name])
    raise ValueError(f"unknown classification kind: {blob['kind']}")


def _approx(a: float, b: float, tol: float = 1e-9) -> bool:
    return math.isclose(a, b, rel_tol=tol, abs_tol=tol)


