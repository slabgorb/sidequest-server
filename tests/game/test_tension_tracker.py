# =========================================================================
# AC2 — Multi-round scenario parity (escalating, stalling, reversal)
#       Each scenario file lists steps; replay against TensionTracker and
#       assert every intermediate state matches Rust output byte-for-byte.
# =========================================================================


def _replay_scenario(fixture_name: str) -> None:
    fixture = _load_fixture(fixture_name)
    th = DramaThresholds(**fixture["thresholds"])
    tracker = TensionTracker()

    # Guard against an empty fixture silently passing the parity gate
    # (matches the len(cases) > 0 guard in the classify_*_fixture_parity
    # tests above).
    assert len(fixture["steps"]) > 0, f"scenario fixture {fixture_name} has no steps"

    failures: list[str] = []

    for idx, step in enumerate(fixture["steps"]):
        kind = step["step"]["kind"]
        expected = step["after"]

        if kind == "observe":
            r = _round_from_fixture(step["step"]["round"])
            killed = step["step"]["round"].get("killed")
            ratio = step["step"].get("lowest_hp_ratio")
            cls = tracker.observe(r, killed, ratio)
            expected_cls = _classification_from_fixture(expected["classification"])
            if cls != expected_cls:
                failures.append(
                    f"step {idx}: classification expected {expected_cls!r}, got {cls!r}"
                )
        elif kind == "tick":
            tracker.tick()
        elif kind == "update_stakes":
            tracker.update_stakes(step["step"]["current_hp"], step["step"]["max_hp"])
        else:  # pragma: no cover - defensive
            raise ValueError(f"unknown step kind: {kind}")

        if not _approx(tracker.action_tension(), expected["action_tension"]):
            failures.append(
                f"step {idx}: action_tension expected {expected['action_tension']}, "
                f"got {tracker.action_tension()}"
            )
        if not _approx(tracker.stakes_tension(), expected["stakes_tension"]):
            failures.append(
                f"step {idx}: stakes_tension expected {expected['stakes_tension']}, "
                f"got {tracker.stakes_tension()}"
            )
        if not _approx(tracker.drama_weight(), expected["drama_weight"]):
            failures.append(
                f"step {idx}: drama_weight expected {expected['drama_weight']}, "
                f"got {tracker.drama_weight()}"
            )
        if not _approx(tracker.active_spike(), expected["active_spike"]):
            failures.append(
                f"step {idx}: active_spike expected {expected['active_spike']}, "
                f"got {tracker.active_spike()}"
            )
        if tracker.boring_streak() != expected["boring_streak"]:
            failures.append(
                f"step {idx}: boring_streak expected {expected['boring_streak']}, "
                f"got {tracker.boring_streak()}"
            )

        hint = tracker.pacing_hint(th)
        ph_expected = expected["pacing_hint"]
        if not _approx(hint.drama_weight, ph_expected["drama_weight"]):
            failures.append(
                f"step {idx}: pacing_hint.drama_weight expected "
                f"{ph_expected['drama_weight']}, got {hint.drama_weight}"
            )
        if hint.target_sentences != ph_expected["target_sentences"]:
            failures.append(
                f"step {idx}: pacing_hint.target_sentences expected "
                f"{ph_expected['target_sentences']}, got {hint.target_sentences}"
            )
        if hint.delivery_mode.name != ph_expected["delivery_mode"]:
            failures.append(
                f"step {idx}: pacing_hint.delivery_mode expected "
                f"{ph_expected['delivery_mode']}, got {hint.delivery_mode.name}"
            )
        if hint.escalation_beat != ph_expected["escalation_beat"]:
            failures.append(
                f"step {idx}: pacing_hint.escalation_beat expected "
                f"{ph_expected['escalation_beat']!r}, got {hint.escalation_beat!r}"
            )
        if hint.narrator_directive() != ph_expected["narrator_directive"]:
            failures.append(
                f"step {idx}: narrator_directive expected "
                f"{ph_expected['narrator_directive']!r}, got "
                f"{hint.narrator_directive()!r}"
            )

    assert not failures, f"scenario '{fixture_name}' parity drift:\n" + "\n".join(failures)


def test_scenario_escalating_parity():
    _replay_scenario("scenario_escalating.json")


def test_scenario_stalling_parity():
    _replay_scenario("scenario_stalling.json")


