def test_boring_turn_increases_action_tension():
    tracker = TensionTracker()
    tracker.record_event(CombatEvent.Boring)
    assert tracker.action_tension() > 0.0, "boring turn should ramp action tension"


def test_consecutive_boring_turns_ramp_faster():
    tracker = TensionTracker()
    tracker.record_event(CombatEvent.Boring)
    after_one = tracker.action_tension()
    tracker.record_event(CombatEvent.Boring)
    after_two = tracker.action_tension()
    assert (after_two - after_one) > after_one, (
        f"gambler's ramp should accelerate: first bump {after_one}, "
        f"second bump {after_two - after_one}"
    )


def test_dramatic_event_drops_action_tension():
    tracker = TensionTracker()
    for _ in range(5):
        tracker.record_event(CombatEvent.Boring)
    before = tracker.action_tension()
    tracker.record_event(CombatEvent.Dramatic)
    assert tracker.action_tension() < before, "dramatic event should drop action tension"


def test_dramatic_event_resets_boring_streak():
    tracker = TensionTracker()
    for _ in range(3):
        tracker.record_event(CombatEvent.Boring)
    tracker.record_event(CombatEvent.Dramatic)
    tracker.record_event(CombatEvent.Boring)
    fresh_boring = tracker.action_tension()
    fresh = TensionTracker()
    fresh.record_event(CombatEvent.Boring)
    assert fresh_boring == fresh.action_tension(), (
        "after dramatic reset, boring ramp should restart from scratch"
    )


