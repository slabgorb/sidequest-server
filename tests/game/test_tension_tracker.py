"""Tests for sidequest.game.tension_tracker — dual-track pacing model.

Port of ``sidequest-api/crates/sidequest-game/src/tension_tracker.rs``.

Port discipline (per ADR-082 / Epic 42 execution-strategy spec):

- Every Rust ``#[test]`` becomes one pytest function with the same name.
- No idiomatic rewrites — Rust source is the behavioural contract.
- Classification-table parity is enforced via JSON fixtures generated by
  the Rust example at
  ``sidequest-api/crates/sidequest-game/examples/tension_fixture_export.rs``.
  Regenerate with ``cargo run --example tension_fixture_export``.
- Multi-round scenarios (escalating, stalling, reversal) are full
  observe→pacing_hint sequences captured from Rust as committed JSON;
  the Python port replays them step-for-step and asserts every
  intermediate state matches.

The Python ``TensionTracker`` API mirrors Rust:

- ``TensionTracker()`` / ``TensionTracker.with_values(action, stakes)``
- ``action_tension() / stakes_tension() / drama_weight() / active_spike() / boring_streak()``
- ``inject_spike(amount)``
- ``record_event(CombatEvent)``
- ``update_stakes(current_hp, max_hp)``
- ``tick()``  (no args — decays action_tension and ages spike)
- ``observe(round, killed, lowest_hp_ratio) -> TurnClassification``
- ``pacing_hint(thresholds: DramaThresholds) -> PacingHint``

Free functions:

- ``classify_round(round, killed) -> CombatEvent``
- ``classify_combat_outcome(round, killed, lowest_hp_ratio) -> TurnClassification``

Spec deviations vs context-story-42-3.md (logged in session file):

- AC2 names ``tracker.tick(round_result)``; Rust API is
  ``observe(round, killed, lowest_hp_ratio)`` then ``pacing_hint(thresholds)``.
- AC3 zone is ``AttentionZone.Late`` (Rust source), not Early/Valley.
- ``TurnContext.pacing_hint`` is typed ``PacingHint | None``, not ``str | None``.
"""

