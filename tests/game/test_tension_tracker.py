# AC4 — DramaThresholds sourced from genre pack: thresholds drive shifts.
#        A pack with a lower streaming_delivery_min should flip a mid-drama
#        tracker into Streaming where defaults would yield Sentence.
# =========================================================================


def test_pacing_hint_threshold_shift_lowers_streaming_floor():
    tracker = TensionTracker.with_values(0.5, 0.0)

    defaults = DramaThresholds()  # streaming_delivery_min defaults to 0.70
    default_hint = tracker.pacing_hint(defaults)
    assert default_hint.delivery_mode == DeliveryMode.Sentence, (
        f"with default thresholds at drama 0.5, expected Sentence delivery, got {default_hint.delivery_mode.name}"
    )

    aggressive = DramaThresholds(streaming_delivery_min=0.40, sentence_delivery_min=0.20)
    custom_hint = tracker.pacing_hint(aggressive)
    assert custom_hint.delivery_mode == DeliveryMode.Streaming, (
        f"with aggressive thresholds at drama 0.5, expected Streaming, got {custom_hint.delivery_mode.name}"
    )


def test_pacing_hint_escalation_streak_threshold_shift():
    tracker = TensionTracker()
    for _ in range(3):
        tracker.record_event(CombatEvent.Boring)

    # Defaults: escalation_streak=5 → no beat at streak 3.
    defaults = DramaThresholds()
    assert tracker.boring_streak() == 3
    assert tracker.pacing_hint(defaults).escalation_beat is None

    # Lower the escalation threshold below current streak — beat should fire.
    relaxed = DramaThresholds(escalation_streak=2)
    beat = tracker.pacing_hint(relaxed).escalation_beat
    assert beat is not None and "environment shifts" in beat


